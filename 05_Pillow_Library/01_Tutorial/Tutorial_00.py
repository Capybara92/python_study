#Pillow

#Pillow는 Alex Clark과와 Contributors가 만든 PIL fork 이다.
#PIL은 Fredrik Lundh 및 Contributors의 Python Imaging Library이다.

#Python Imaging Library는 Python 인터프리터에 이미지 처리 기능을 추가한다.
#이 라이브러리는 광범위한 파일 형식 지원, 효율적인 내부 표현 및 상당히 강력한 이미지 처리 기능을 제공한다.
#핵심 이미지 라이브러리는 몇 가지 기본 픽셀 형식으로 저장된 데이터에 빠르게 액세스 할 수 있도록 설계되었다.
#일반적인 이미지 처리도구를 위한 견고한 기반을 제공해야한다.
